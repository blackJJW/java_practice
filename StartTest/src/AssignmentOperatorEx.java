
public class AssignmentOperatorEx {

	public static void main(String[] args) {
		int result = 0;
		
		System.out.println("result : " + result);
		
		result += 10;
		System.out.println("result : " + result);
		result -= 1;
		System.out.println("result : " + result);
		result *= 2;
		System.out.println("result : " + result);
		result /= 3;
		System.out.println("result : " + result);
		result %= 4;
		System.out.println("result : " + result);
		result &= 2;
		System.out.println("result : " + result);
		result |= 1;
		System.out.println("result : " + result);
		result ^= 2;
		System.out.println("result : " + result);
		result <<= 5;
		System.out.println("result : " + result);
		result >>= 2;
		System.out.println("result : " + result);
		result >>>= 3;
		System.out.println("result : " + result);
	}

}
